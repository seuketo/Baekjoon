/*
문제

UCF 프로그래밍 팀의 코치들은 가을과 봄에 정기적으로 실습을 계획합니다(모든 UCF 학생들은 실습을 환영합니다). 
여름 동안 대부분의 팀원들은 떠나지만 코치들은 학생들이 "녹슬지" 않도록 하는 방법을 알고 있습니다. 
코치들은 보통 여름 동안 얼마나 많은 문제를 해결했는지에 따라 팀원들에게 보상을 줍니다. 
예를 들어 코치가 $1,000의 상금을 가지고 있고 학생이 3명이라고 가정해 보겠습니다. 
또한 세 명의 학생이 각각 5, 8, 7개의 문제, 즉 총 20개의 문제를 풀었다고 가정해 봅시다. 
따라서 한 문제에 대한 보상은 $50($1000/20)이며 세 명의 팀원은 각각 $250, $400 및 $350를 받게 됩니다.
총 상금과 각 팀원이 해결한 문제의 수를 고려하여 각 학생의 보상을 계산합니다.

입력

첫째 줄에 팀원 수를 나타내는 
n(1 <= n <= 30)과 학생들에게 나누어 줄 달러 금액을 나타내는 d(1 <= d <= 30,000)가 공백으로 구분되어 주어집니다. 
다음 n개의 줄에 각각의 팀원이 해결한 문제의 수를 나타내는 1 이상 300 이하의 정수가 주어집니다. 
한 문제에 대한 보상은 정수임이 보장됩니다.

출력

각 팀원이 받을 수 있는 보상을 달러로 출력합니다. (입력에 주어진 것과 동일한 순서로).
https://www.acmicpc.net/submit/25858
 */
import java.io.*;
import java.util.StringTokenizer;
public class a_25858 {
    BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
    StringBuilder sb = new StringBuilder();
    public void run() throws IOException{
        StringTokenizer st = new StringTokenizer(br.readLine());
        int n = Integer.parseInt(st.nextToken()), d = Integer.parseInt(st.nextToken());
        int[] arr = new int[n];
        int sum = 0;
        for (int i = 0; i < n; i++) {
            arr[i] = Integer.parseInt(br.readLine());
            sum += arr[i];
        }
        for (int i = 0; i < n; i++) sb.append(arr[i] * (d / sum) + "\n");
        System.out.print(sb);
        br.close();
    }
    public static void main(String[] args) throws IOException{
        new a_25858().run();
    }
}
